#define OLED_SPI_CS(x)   ((x) ? DL_GPIO_setPins(OLED_SPI_CS_PORT, OLED_SPI_CS_PIN)   : DL_GPIO_clearPins(OLED_SPI_CS_PORT, OLED_SPI_CS_PIN))
#define OLED_SPI_DC(x)   ((x) ? DL_GPIO_setPins(OLED_SPI_DC_PORT, OLED_SPI_DC_PIN)   : DL_GPIO_clearPins(OLED_SPI_DC_PORT, OLED_SPI_DC_PIN))
#define OLED_SPI_RES(x)  ((x) ? DL_GPIO_setPins(OLED_SPI_RES_PORT, OLED_SPI_RES_PIN) : DL_GPIO_clearPins(OLED_SPI_RES_PORT, OLED_SPI_RES_PIN))
#define OLED_SPI_SDA(x)  ((x) ? DL_GPIO_setPins(OLED_SPI_SDA_PORT, OLED_SPI_SDA_PIN) : DL_GPIO_clearPins(OLED_SPI_SDA_PORT, OLED_SPI_SDA_PIN))
#define OLED_SPI_SCL(x)  ((x) ? DL_GPIO_setPins(OLED_SPI_SCL_PORT, OLED_SPI_SCL_PIN) : DL_GPIO_clearPins(OLED_SPI_SCL_PORT, OLED_SPI_SCL_PIN))
#define OLED_SPI_BLK(x)  ((x) ? DL_GPIO_setPins(OLED_SPI_BLK_PORT, OLED_SPI_BLK_PIN) : DL_GPIO_clearPins(OLED_SPI_BLK_PORT, OLED_SPI_BLK_PIN))

/* ==================== 局部延�?==================== */
static void delay_us(uint32_t us) {
    for (uint32_t i = 0; i < us; i++) {
        delay_cycles(32);
    }
}

static void delay_ms(uint32_t ms) {
    for (uint32_t i = 0; i < ms; i++) {
        delay_us(1000);
    }
}

/* ==================== SPI 底层 ==================== */
static void OLED_SPI_Start(void) { OLED_SPI_CS(0); }
static void OLED_SPI_Stop(void)  { OLED_SPI_CS(1); }

static void OLED_SendByte(uint8_t dat) {
    for (uint8_t i = 0; i < 8; i++) {
        OLED_SPI_SCL(0);
        OLED_SPI_SDA((dat & 0x80) ? 1 : 0);
        delay_us(1);
        OLED_SPI_SCL(1);
        delay_us(1);
        dat <<= 1;
    }
    OLED_SPI_SCL(0);
}

/* 高字节先�?*/
static void OLED_SendWord(uint16_t dat) {
    OLED_SendByte((uint8_t)(dat >> 8));
    OLED_SendByte((uint8_t)(dat & 0xFF));
}

static void OLED_WriteCommand(uint8_t cmd) {
    OLED_SPI_Start();
    OLED_SPI_DC(0);
    OLED_SendByte(cmd);
    OLED_SPI_Stop();
}

static void OLED_WriteData(uint8_t dat) {
    OLED_SPI_Start();
    OLED_SPI_DC(1);
    OLED_SendByte(dat);
    OLED_SPI_Stop();
}

/* ==================== 颜色 ==================== */
uint16_t OLED_ForeColor = COLOR_WHITE;
uint16_t OLED_BackColor = COLOR_BLACK;

/* ==================== 方向与分辨率 ==================== */
uint8_t  OLED_Orientation = OLED_ORIENTATION_PORTRAIT;
uint16_t OLED_Width   = 80;
uint16_t OLED_Height  = 160;
uint16_t OLED_X_Off   = 26;
uint16_t OLED_Y_Off   = 1;

/* ==================== 窗口设置 (含行列偏移) ==================== */
static void OLED_SetWindow(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
    uint16_t xs = x0 + OLED_X_Off;
    uint16_t xe = x1 + OLED_X_Off;
    uint16_t ys = y0 + OLED_Y_Off;
    uint16_t ye = y1 + OLED_Y_Off;

    OLED_WriteCommand(0x2A); // CASET
    OLED_WriteData(xs >> 8); OLED_WriteData(xs & 0xFF);
    OLED_WriteData(xe >> 8); OLED_WriteData(xe & 0xFF);

    OLED_WriteCommand(0x2B); // RASET
    OLED_WriteData(ys >> 8); OLED_WriteData(ys & 0xFF);
    OLED_WriteData(ye >> 8); OLED_WriteData(ye & 0xFF);

    OLED_WriteCommand(0x2C); // RAMWR
}

/* ==================== 绘制 ==================== */
void OLED_DrawPixel_Ex(uint8_t x, uint8_t y, uint16_t color) {
    if (x >= OLED_Width || y >= OLED_Height) return;
    OLED_SetWindow(x, y, x, y);
    OLED_SPI_Start();
    OLED_SPI_DC(1);
    OLED_SendWord(color);
    OLED_SPI_Stop();
}

void OLED_Fill(uint16_t color) {
    OLED_SetWindow(0, 0, OLED_Width - 1, OLED_Height - 1);
    OLED_SPI_Start();
    OLED_SPI_DC(1);
    for (uint16_t y = 0; y < OLED_Height; y++) {
        for (uint16_t x = 0; x < OLED_Width; x++) {
            OLED_SendWord(color);
        }
    }
    OLED_SPI_Stop();
}

void OLED_FillRow(uint8_t Line, uint16_t color) {
    uint16_t y0 = (uint16_t)(Line - 1) * 16;
    uint16_t y1 = y0 + 15;
    if (y0 >= OLED_Height) return;
    if (y1 >= OLED_Height) y1 = OLED_Height - 1;

    OLED_SetWindow(0, y0, OLED_Width - 1, y1);
    OLED_SPI_Start();
    OLED_SPI_DC(1);
    for (uint16_t y = y0; y <= y1; y++) {
        for (uint16_t x = 0; x < OLED_Width; x++) {
            OLED_SendWord(color);
        }
    }
    OLED_SPI_Stop();
}

