void OLED_CalibrateOffset(void) {
    /* 在纯色背景下画四个边角白点，方便判断偏移方向 */
    OLED_Fill(COLOR_RED);
    OLED_DrawPixel_Ex(0, 0, COLOR_WHITE);              // 左上
    OLED_DrawPixel_Ex(OLED_Width - 1, 0, COLOR_WHITE);
    OLED_DrawPixel_Ex(0, OLED_Height - 1, COLOR_WHITE);
    OLED_DrawPixel_Ex(OLED_Width - 1, OLED_Height - 1, COLOR_WHITE);
}

/* ==================== ST7735S 精简初始�?==================== */
void OLED_SetOrientation(uint8_t mode) {
    OLED_Orientation = mode;
    switch (mode) {
        case OLED_ORIENTATION_LANDSCAPE:
            OLED_Width = 160; OLED_Height = 80;
            OLED_X_Off = 1; OLED_Y_Off = 26;
            OLED_WriteCommand(0x36); OLED_WriteData(0x68);
            break;
        case OLED_ORIENTATION_PORTRAIT_REV:
            OLED_Width = 80; OLED_Height = 160;
            OLED_X_Off = 26; OLED_Y_Off = 1;
            OLED_WriteCommand(0x36); OLED_WriteData(0x08);
            break;
        case OLED_ORIENTATION_LANDSCAPE_REV:
            OLED_Width = 160; OLED_Height = 80;
            OLED_X_Off = 1; OLED_Y_Off = 26;
            OLED_WriteCommand(0x36); OLED_WriteData(0xA8);
            break;
        default: // PORTRAIT
            OLED_Width = 80; OLED_Height = 160;
            OLED_X_Off = 26; OLED_Y_Off = 1;
            OLED_WriteCommand(0x36); OLED_WriteData(0xC8);
            break;
    }
}

void OLED_Init(void) {
    delay_ms(50);
    OLED_SPI_BLK(1);
    OLED_SPI_RES(0); delay_ms(100);
    OLED_SPI_RES(1); delay_ms(50);
    OLED_WriteCommand(0x11); delay_ms(120);
    OLED_WriteCommand(0x3A); OLED_WriteData(0x05);
    OLED_WriteCommand(0x21);
    OLED_WriteCommand(0x29);
    OLED_SetOrientation(OLED_ORIENTATION_PORTRAIT);
    OLED_Fill(COLOR_BLACK);
}
