void OLED_ShowChinese16(uint8_t x, uint8_t y, const uint8_t bitmap[32]) {
    for (uint8_t row = 0; row < 16; row++) {
        uint8_t b1 = bitmap[row * 2];
        uint8_t b2 = bitmap[row * 2 + 1];
        for (uint8_t col = 0; col < 8; col++) {
            if (b1 & (0x80 >> col))
                OLED_DrawPixel_Ex(x + col, y + row, OLED_ForeColor);
        }
        for (uint8_t col = 0; col < 8; col++) {
            if (b2 & (0x80 >> col))
                OLED_DrawPixel_Ex(x + 8 + col, y + row, OLED_ForeColor);
        }
    }
}

void OLED_ShowChar(uint8_t Line, uint8_t Column, char Char, uint16_t color) {
    uint8_t x = (Column - 1) * 8;
    uint8_t y = (Line - 1) * 16;
    uint8_t idx = (uint8_t)(Char - ' ');
    if (idx >= 95) idx = 0;

    const uint8_t *p = OLED_F8x16[idx];

    OLED_SetWindow(x, y, x + 7, y + 15);
    OLED_SPI_Start();
    OLED_SPI_DC(1);
    for (uint8_t row = 0; row < 16; row++) {
        for (uint8_t col = 0; col < 8; col++) {
            if (p[row] & (0x80 >> col))
                OLED_SendWord(color);
            else
                OLED_SendWord(OLED_BackColor);
        }
    }
    OLED_SPI_Stop();
}

void OLED_ShowString(uint8_t Line, uint8_t Column, char *String, uint16_t color) {
    for (uint8_t i = 0; String[i] != '\0'; i++) {
        if ((Column - 1 + i) * 8 + 7 >= OLED_Width) break;
        OLED_ShowChar(Line, Column + i, String[i], color);
    }
}

/* ==================== 辅助函数 ==================== */
static uint32_t OLED_Pow(uint32_t X, uint32_t Y) {
    uint32_t Result = 1;
    while (Y--) Result *= X;
    return Result;
}

void OLED_ShowNum(uint8_t Line, uint8_t Column, uint32_t Number, uint8_t Length) {
    for (uint8_t i = 0; i < Length; i++) {
        OLED_ShowChar(Line, Column + i, Number / OLED_Pow(10, Length - i - 1) % 10 + '0', OLED_ForeColor);
    }
}

void OLED_ShowSignedNum(uint8_t Line, uint8_t Column, int32_t Number, uint8_t Length) {
    uint32_t Number1;
    if (Number >= 0) {
        OLED_ShowChar(Line, Column, '+', OLED_ForeColor);
        Number1 = Number;
    } else {
        OLED_ShowChar(Line, Column, '-', OLED_ForeColor);
        Number1 = -Number;
    }
    for (uint8_t i = 0; i < Length; i++) {
        OLED_ShowChar(Line, Column + i + 1, Number1 / OLED_Pow(10, Length - i - 1) % 10 + '0', OLED_ForeColor);
    }
}

void OLED_ShowHexNum(uint8_t Line, uint8_t Column, uint32_t Number, uint8_t Length) {
    for (uint8_t i = 0; i < Length; i++) {
        uint8_t nibble = Number / OLED_Pow(16, Length - i - 1) % 16;
        OLED_ShowChar(Line, Column + i, (nibble < 10) ? (nibble + '0') : (nibble - 10 + 'A'), OLED_ForeColor);
    }
}

void OLED_ShowBinNum(uint8_t Line, uint8_t Column, uint32_t Number, uint8_t Length) {
    for (uint8_t i = 0; i < Length; i++) {
        OLED_ShowChar(Line, Column + i, Number / OLED_Pow(2, Length - i - 1) % 2 + '0', OLED_ForeColor);
    }
}

/* ==================== 菜单行渲染 ==================== */
void OLED_MenuRow(uint8_t Line, const char *text, uint16_t textColor, uint16_t selColor, uint8_t selected) {
    uint16_t oldBack = OLED_BackColor;
    if (selected) {
        OLED_FillRow(Line, selColor);
        OLED_BackColor = selColor;   // 字符背景色匹配高亮色
    } else {
        OLED_FillRow(Line, COLOR_BLACK);
    }
    OLED_ShowString(Line, 1, (char *)text, textColor);
    OLED_BackColor = oldBack;
}

